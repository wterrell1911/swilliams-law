export function Sidebar({ profile, onClose }: SidebarProps) {
  const pathname = usePathname()
  const isAdmin = profile?.role === "admin"

  return (
    <aside className="flex flex-col h-full bg-navy-950 border-r border-navy-800 w-64">
      <div className="p-6 border-b border-navy-800">
        <Link href="/portal/dashboard" className="flex items-center gap-2" onClick={onClose}>
          <LogoIcon />
          <span className="text-warmWhite/60 text-xs font-medium uppercase tracking-wider">Portal</span>
        </Link>
      </div>

      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {clientNav.map((item) => {
          const isActive = pathname === item.href
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={onClose}
              className={cn(
                "flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors",
                isActive
                  ? "bg-gold-500/10 text-gold-500"
                  : "text-warmWhite/70 hover:bg-navy-800 hover:text-warmWhite"
              )}
            >
              <item.icon className="w-5 h-5 flex-shrink-0" />
              {item.name}
            </Link>
          )
        })}

        {isAdmin && (
          <>
            <div className="pt-4 pb-2 px-3">
              <p className="text-[11px] font-semibold uppercase tracking-wider text-warmWhite/40">
                Admin
              </p>
            </div>
            {adminNav.map((item) => {
              const isActive = pathname === item.href || (item.href !== "/portal/admin" && pathname.startsWith(item.href))
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  onClick={onClose}
                  className={cn(
                    "flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors",
                    isActive
                      ? "bg-gold-500/10 text-gold-500"
                      : "text-warmWhite/70 hover:bg-navy-800 hover:text-warmWhite"
                  )}
                >
                  <item.icon className="w-5 h-5 flex-shrink-0" />
                  {item.name}
                </Link>
              )
            })}

            <div className="pt-4 pb-2 px-3">
              <p className="text-[11px] font-semibold uppercase tracking-wider text-warmWhite/40">
                CRM
              </p>
            </div>
            {crmNav.map((item) => {
              const isActive = pathname === item.href || (item.href !== "/portal/crm" && pathname.startsWith(item.href))
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  onClick={onClose}
                  className={cn(
                    "flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors",
                    isActive
                      ? "bg-gold-500/10 text-gold-500"
                      : "text-warmWhite/70 hover:bg-navy-800 hover:text-warmWhite"
                  )}
                >
                  <item.icon className="w-5 h-5 flex-shrink-0" />
                  {item.name}
                </Link>
              )
            })}
          </>
        )}
      </nav>

      <div className="p-4 border-t border-navy-800">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-full bg-gold-500/20 flex items-center justify-center text-gold-500 text-xs font-bold">
            {profile?.contact_name?.charAt(0)?.toUpperCase() || "?"}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-warmWhite truncate">
              {profile?.contact_name || "User"}
            </p>
            <p className="text-xs text-warmWhite/50 truncate">
              {profile?.firm_name || profile?.email}
            </p>
          </div>
        </div>
      </div>
    </aside>
  )
}

